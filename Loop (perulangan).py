# Perulangan (Loop)

#for kondisi: 
#    aksi

# ini dengan list
angka_list = [1,3,5,7,9] # ini adalah list
print(angka_list)

for i in angka_list:
    print(f"i sekarang -> {i}")
print("Akhir dari program \n")

# ini dengan range
angka_range = range(6)

for i in angka_range:
    print(f"i sekarang -> {i}")
print("Akhir dari program 2 \n")

angka_range1 = range(1,6)

for i in angka_range1:
    print(f"i sekarang -> {i}")
print("Akhir dari program 3 \n")

# menggunakan string

data_str = "keren"
for i in data_str:
    print(i)

