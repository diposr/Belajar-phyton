# operasi yang dilakukan dengan penyingkatan
# operasi yang ditambah dengan assignment

a = 5 # assignment
print("nilai dari a adalah",a)

a += 1 # artinya adalah a = a + 1
print("milai a += 1, maka nilai a menjadi",a)

a -= 2 # artinya adalah a = a - 2
print("nilai a -= 2, maka nilai a menjadi",a)

a *= 5 # artinya adalah a = a * 5
print("nilai a *= 5, maka nilai a menjadi",a)

a /= 2 # artinya adalah a = a / 2
print("nilai a /= 2, maka nilai a menjadi",a)

# modulus dan floor division

b = 10
print("\nnilai b =",b)

b %= 3
print("nilai b %= 3, maka nilai b menjadi",b)

b = 10 
print("\nnilai b=",b)

b //= 3
print("nilai b //= 3, maka nilai b menjadi",b)

a = 5
print("\nnilai a =",a)

# pangkat atau eksponen

a **= 3
print("nilai a **= 3, maka nilai a menjadi",a)

