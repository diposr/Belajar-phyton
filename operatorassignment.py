# operasi bitwise

# OR

c = True
print("\nnilai dari c =",c)
c != False
print("nilai dari c!= false, maka nilai c menjadi",c)
c = False
print("nilai dari c=",c)
c != False
print("nilai dari c!= false, maka nilai c menjadi",c)

# AND
c = True
print("\nnilai dari c =",c)
c &= False
print("nilai dari c&= false, maka nilai c menjadi",c)
c = True
print("nilai dari c=",c)
c &= True
print("nilai dari c&= true, maka nilai c menjadi",c)

# XOR
c = True
print("\nnilai dari c =",c)
c ^= False
print("nilai dari c^= false, maka nilai c menjadi",c)
c = True
print("nilai dari c=",c)
c ^= True
print("nilai dari c^= true, maka nilai c menjadi",c)

